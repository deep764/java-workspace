package Module_2;

import java.util.HashSet;

public class specified_element_hashset 
{
	 public static void main(String[] args) {
	        // Create a new HashSet
	        HashSet<String> set = new HashSet<>();

	        // Add some elements to the set
	        set.add("element 1");
	        set.add("element 2");
	        set.add("element 3");

	        // Append a new element to the end of the set
	        set.add("element 4");

	        // Print all the elements in the set
	        System.out.println("Elements in the set: " + set);
	    }
}
