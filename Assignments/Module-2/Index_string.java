package Module_2;

public class Index_string 
{
	public static void main(String[] args) 
	{
		String str = "Tops Technologies!";
        int index1 = 0;
        int index2 = 10;
        
        char ch1 = str.charAt(index1);
        char ch2 = str.charAt(index2);
        
        System.out.println("The character at position " + index1 + " is " + ch1);
        System.out.println("The character at position " + index2 + " is " + ch2);
	}
}
